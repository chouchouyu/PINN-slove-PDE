import numpy as np

def filter_positive_indices_where(arr):
    """
    使用np.where方法筛选大于0的索引
    返回：包含所有大于0元素索引的元组
    """
    indices = np.where(arr > 0)
    return indices[0]  # 因为是一维数组，取第一个元素

def filter_positive_indices_argwhere(arr):
    """
    使用np.argwhere方法筛选大于0的索引
    返回：二维数组，每行包含一个满足条件的元素的索引
    """
    indices = np.argwhere(arr > 0)
    return indices.flatten()  # 展平为一维数组

def filter_positive_indices_flatnonzero(arr):
    """
    使用np.flatnonzero方法筛选大于0的索引
    返回：一维数组，包含所有大于0元素的索引
    """
    indices = np.flatnonzero(arr > 0)
    return indices

def filter_positive_indices_list_comprehension(arr):
    """
    使用列表推导式筛选大于0的索引
    返回：列表，包含所有大于0元素的索引
    """
    indices = [i for i, val in enumerate(arr) if val > 0]
    return indices

def get_positive_values_by_indices(arr, indices):
    """
    根据索引获取对应的元素值
    返回：包含所有大于0元素的数组
    """
    return arr[indices]

def analyze_positive_elements(arr):
    """
    分析大于0的元素
    返回：包含统计信息的字典
    """
    positive_indices = filter_positive_indices_where(arr)
    positive_values = get_positive_values_by_indices(arr, positive_indices)
    
    return {
        'total_elements': len(arr),
        'positive_count': len(positive_indices),
        'zero_count': len(arr) - len(positive_indices),
        'positive_percentage': len(positive_indices) / len(arr) * 100,
        'max_positive_value': np.max(positive_values) if len(positive_values) > 0 else 0,
        'min_positive_value': np.min(positive_values) if len(positive_values) > 0 else 0,
        'mean_positive_value': np.mean(positive_values) if len(positive_values) > 0 else 0,
        'std_positive_value': np.std(positive_values) if len(positive_values) > 0 else 0,
        'positive_indices': positive_indices,
        'positive_values': positive_values
    }

def compare_methods(arr):
    """
    比较不同筛选方法的性能和结果一致性
    """
    import time
    
    methods = [
        ("np.where", filter_positive_indices_where),
        ("np.argwhere", filter_positive_indices_argwhere),
        ("np.flatnonzero", filter_positive_indices_flatnonzero),
        ("列表推导式", filter_positive_indices_list_comprehension)
    ]
    
    results = {}
    for name, func in methods:
        start_time = time.perf_counter()
        indices = func(arr)
        end_time = time.perf_counter()
        results[name] = {
            'indices': indices,
            'time': end_time - start_time,
            'count': len(indices)
        }
    
    return results

